#  9. strip(), lstrip(), rstrip() ---> (Remove Spaces):

text="          I am Abrar          "
print(text.strip())                     # output ---> I am Abrar.
print()

print(text.lstrip())                    # output ---> I am Abrar(empty space)
print()

print(text.rstrip())                    # output ---> (empty space) I am Abrar
print()