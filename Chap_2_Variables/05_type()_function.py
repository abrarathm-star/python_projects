# type() function is used to find the data type of a given variable in python.

a=20
j=type(a)   #<class 'int'>
print(j)

b=12.67
k=type(b)   #<class 'float'>
print(k)

c="Abrar"
l=type(c)   #<class 'str'>
print(l)    

d=True
m=type(d)
print(m)    #<class 'bool'>

e=False
n=type(e)   #<class 'bool'> 
print(n)    

f=None
o=type(f)   #<class 'NoneType'>
print(o)