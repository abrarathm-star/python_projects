# COMPARISON OPERATORS:

aa = 5>6  #it will show false
print(aa)

bb= 4<5   # it will show true
print(bb)

ac=6>=6   # it will show true (because '>=' means greater than or equals to)
print(ac)

dj=2<=2    # it will show true (because '<=' means less than or equals to)
print(dj)


j=2!=2   #it will show false (because '!=' means not equal to)
print(j)

z=4!=5   # it will show true
print(z)


kkr = 8==8   #  it will show true (== is used if we want to check if two values are equal or not, it is a comparison operator)
            #  (= is used when we want to assign a vule to a variable, it is an assignment variable)

print(kkr)